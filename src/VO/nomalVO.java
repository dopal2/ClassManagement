package VO;


public class nomalVO {
	private String name;
	private String tel;
	private String email;
	
	public String getname() {
		return name;
	}
	public String gettel() {
		return tel;
	}
	public String getemail() {
		return email;
	}
	public void setname(String name) {
		this.name = name;
	}
	public void settel(String tel) {
		this.tel = tel;
	}
	public void setemail(String email) {
		this.email = email;
	}
}
